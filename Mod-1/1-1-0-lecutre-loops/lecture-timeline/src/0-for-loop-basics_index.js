// standard
for (let i = 0; i < 11; i++) {
  console.log(i);
}

// change the start
for (let i = 3; i < 11; i++) {
  console.log(i);
}

// change the end
for (let i = 0; i < 21; i++) {
  console.log(i);
}

// change the increment
for (let i = 0; i < 11; i *= 3) {
  console.log(i);
}

// go down not up
for (let i = 10; i > 0; i--) {
  console.log(i);
}
